public class MapEntry 
{
    private String value;
    private int count;
    
    /******************************************************
    ‘***  MapEntry
    ‘***  Nick Crans
    ‘******************************************************
    ‘*** Stores the value parsed from the text document and
    ‘*** the count
    ‘******************************************************
    ‘*** 10/31/2017
    ‘******************************************************/
    MapEntry(String val, int count)
    {
        value = val;
        this.count = count;
    }
    
    /******************************************************
    ‘***  getValue
    ‘***  Nick Crans
    ‘******************************************************
    ‘*** Returns the value stored in the object
    ‘******************************************************
    ‘*** 10/31/2017
    ‘******************************************************/
    public String getValue()
    {
        return value;
    }
    
    /******************************************************
    ‘***  getCount
    ‘***  Nick Crans
    ‘******************************************************
    ‘*** returns the count stored in the object at the time
    ‘*** the value was inserted.
    ‘******************************************************
    ‘*** 10/31/2017
    ‘******************************************************/
    public int getCount()
    {
        return count;
    }
    
    /******************************************************
    ‘***  setValue
    ‘***  Nick Crans
    ‘******************************************************
    ‘*** If the values must be changed this is the method
    ‘*** that is called, and the new values is passed along
    ‘*** with it.
    ‘******************************************************
    ‘*** 10/31/2017
    ‘******************************************************/
    public void setValue(String val)
    {
        value = val;
    }
    
    /******************************************************
    ‘***  toString
    ‘***  Nick Crans
    ‘******************************************************
    ‘*** returne value
    ‘*** 
    ‘*** 
    ‘******************************************************
    ‘*** 10/31/2017
    ‘******************************************************/
    @Override
    public String toString()
    {
        return value + "\n";
    }
}

